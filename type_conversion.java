class type_conversion
{
 public static void main(String[]args)
 {
  int I=10;
  float F=20.2F;
  double D=123.44;
  {
   byte B=(byte)D;
   System.out.println("print the value:"+B);
   long L=I;
   System.out.println("print the value:"+L);
   short S=(short)F;
   System.out.println("print the value:"+S);
  }
 }
}

 